package pattern;

import java.util.Scanner;

public class floydtri {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int x = sc.nextInt();
        int a = 1;
        for(int i =1 ; i<=x; i++){
            for(int j=1; j<=i; j++){
                System.out.print(a+" ");
                a++;
            }
            System.out.println();
        }
        
        sc.close();
    }
}
